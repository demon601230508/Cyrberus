#include "Position.h"

//--------------------------------------------------------------------------------//
// SETUP POSITION
//--------------------------------------------------------------------------------//

void Position::setup(int current, int *_total)
{
    
    totalOccurences = *_total;
    currentOccurence = current;
    getCurrentPosition();
    
    return;
    
}

//--------------------------------------------------------------------------------//
// RETURN CURRENT POSITION (float between 0 & 1)
//--------------------------------------------------------------------------------//

float Position::getCurrentPosition()
{
    
    currentPosition = (float)currentOccurence / (float)totalOccurences;
    
    return currentPosition;
    
}

