#include "JenClamp.h"

//--------------------------------------------------------------------------------//
// JenClamp
//--------------------------------------------------------------------------------//
/* Rather than simply clamping the value, Jen will roll value back the more it is
 out of range from clamp limits */

float jenClamp(float value, float min, float max)
{
    
    value = ofMap(value, min, max, 0.0f, 1.0f);
    
    while (value > 1 || value < 0) {
        
        if (value > 1){
            value = value - (value-1.0f);
        }
        else if (value < 0){
            value = value * -1.0f;
        }
        
    }
    
    value = ofMap(value, 0.0f, 1.0f, min, max);
    
    return value;
    
}

