#include "GUI.h"

//--------------------------------------------------------------------------------//
// SETUP GUI
//--------------------------------------------------------------------------------//

void GUI::setup()
{
    
    ofBackground(50);
    
    /* Setup main GUI Tab */
    guiTabBar = new ofxUITabBar();
    guiTabBar->setWidth(TAB_BAR_WIDTH);
    guiTabBar->setColorFill(ofxUIColor(200));
    guiTabBar->setColorFillHighlight(ofxUIColor(255));
    guiTabBar->setColorBack(ofxUIColor(20, 20, 20, 150));
    
    /* Setup Gui Global */
    guiGlobal.setup();
    
    return;
    
}

